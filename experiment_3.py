#MEAN
def mean_vec(arr):
  avg=sum(arr)/len(arr)
  return avg
#MEDIAN
def med_vec(arr):
  if(len(arr)%2==0):
    return (sorted(arr)[(len(arr)//2)-1]/2)+(sorted(arr)[len(arr)//2]/2)
  return sorted(arr)[len(arr)//2]
#MODE
def mode_arr(arr):
  max_count = 0
  mode = 0
  for i in arr:
    if arr.count(i) > max_count:
      max_count = arr.count(i)
      mode = i
  return mode
#STANDARD DEVIATION
def standarddev_vec(arr):
  avg=sum(arr)/len(arr)
  var=sum([((x-avg)**2) for x in arr ])/ len(arr)
  return var**(1/2)
